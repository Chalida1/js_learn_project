//ทำซ้ำ for while do while
console.log('--------------------------------');
for(let i = 0; i < 5; i++) {
    console.log("Hello World");
}
console.log('--------------------------------');
let j = 1;
while(j <= 5) { //เป็นจริงทำ
    console.log("Hello World");
    j++;
}
console.log('--------------------------------');
let k = 1;
do { //ทำแล้วค่อยพิสูจน์เงื่อนไข
    console.log("Hello World");
    k++;
} while(k <= 5);
console.log('--------------------------------');


